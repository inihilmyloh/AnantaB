package com.ananta.model;

public class ModelJasa {
    private int IdJasa;
    private String Nama;
    private int Harga;
    
    public int getIdJasa() {
        return IdJasa;
    }

    public void setIdJasa(int idJasa) {
        this.IdJasa = idJasa;
    }

    public String getNama() {
        return Nama;
    }

    public void setNama(String Nama) {
        this.Nama = Nama;
    }

    public int getHarga() {
        return Harga;
    }

    public void setHarga(int Harga) {
        this.Harga = Harga;
    }
}
